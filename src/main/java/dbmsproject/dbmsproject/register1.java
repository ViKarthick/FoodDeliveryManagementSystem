    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
    String customerId = cust_id.getText().trim();
    String customerPassword = new String(password.getPassword()).trim();
    String customerName = name.getText().trim();
    String customerPhone = phone.getText().trim();
    String customerFlatNo = flat_no.getText().trim();
    String customerStreetName = street_name.getText().trim();
    String selectedArea = area_id.getSelectedItem().toString();
    String customerAreaId = selectedArea.split(" - ")[0].trim();
   
    String checkSql = "SELECT cust_id FROM customer WHERE cust_id = ?";
    try (PreparedStatement checkPs = con.prepareStatement(checkSql)) {
        checkPs.setString(1, customerId);
        try (ResultSet rs = checkPs.executeQuery()) {
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Customer ID already exists. Please choose a different ID.");
            } else {
                String insertSql = "INSERT INTO customer (cust_id, password, cust_name, ph_no, flat_no, street_name, area_id) VALUES (?, ?, ?, ?, ?, ?, ?)";
                try (PreparedStatement insertPs = con.prepareStatement(insertSql)) {
                    insertPs.setString(1, customerId);
                    insertPs.setString(2, customerPassword);
                    insertPs.setString(3, customerName);
                    insertPs.setString(4, customerPhone);
                    insertPs.setString(5, customerFlatNo);
                    insertPs.setString(6, customerStreetName);
                    insertPs.setString(7, customerAreaId);
                    insertPs.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Registration successful! Please order next.");
                    orders.main(customerId);
                    this.setVisible(false);
                }
            }
        }
    } catch (SQLException ex) {
        Logger.getLogger(register1.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }

    }//GEN-LAST:event_registerActionPerformed

