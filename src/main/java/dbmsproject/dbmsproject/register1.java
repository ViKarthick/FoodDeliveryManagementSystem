    private void populateAreaComboBox() {
        String sql = "SELECT area_id, area_name FROM area";
        try (PreparedStatement ps = con.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                String areaId = rs.getString("area_id");
                String areaName = rs.getString("area_name");
                String displayText = areaId + " - " + areaName;
                area_id.addItem(displayText); // Add area_id and area_name to the combo box
            }
        } catch (SQLException ex) {
            Logger.getLogger(register1.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error fetching area details: " + ex.getMessage());
        }
    }
